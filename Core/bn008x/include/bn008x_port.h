/**
 * @file bn008x_port.h
 * @brief HAL-абстракция для драйвера BNO08x (GPIO, SPI, таймеры)
 * @author Aleksei
 * @date 2025
 * @version 1.0
 *
 * @details Этот файл определяет интерфейс между драйвером BNO08x и
 *          конкретной аппаратной платформой (STM32, ESP32 и т.д.).
 *          Пользователь должен предоставить реализацию всех функций,
 *          указанных в структуре bn008x_hal_t.
 *
 * @warning Все функции в структуре должны быть реализованы пользователем.
 */

#ifndef BN008X_PORT_H
#define BN008X_PORT_H

#include <stdint.h>
#include "spi.h"
#include "gpio.h"

/**
 * @brief HAL-абстракция для работы с аппаратным обеспечением.
 *
 * Содержит указатели на функции, которые пользователь должен реализовать
 * для своей платформы. Драйвер BNO08x вызывает эти функции вместо прямых
 * обращений к регистрам, что обеспечивает переносимость кода.
 */
typedef struct {
	 /**
	 * @brief Передача данных по SPI.
	 *
	 * @param[in] spi     Указатель на хендл SPI (SPI_HandleTypeDef)
	 * @param[in] tx_data Буфер с данными для передачи
	 * @param[in] len     Количество байт для передачи
	 * @param[in] timeout Максимальное время ожидания (мс)
	 *
	 * @retval 0  Успешная передача
	 * @retval -1 Ошибка передачи (таймаут, аппаратная ошибка)
	 *
	 */
    int32_t (*spi_transmit)(SPI_HandleTypeDef* spi, uint8_t *tx_data, uint16_t len, uint32_t timeout);
    /**
	 * @brief Приём данных по SPI.
	 *
	 * @param[in]  spi     Указатель на хендл SPI (SPI_HandleTypeDef)
	 * @param[out] rx_data Буфер для приёма данных
	 * @param[in]  len     Количество байт для приёма
	 * @param[in]  timeout Максимальное время ожидания (мс)
	 *
	 * @retval 0  Успешный приём
	 * @retval -1 Ошибка приёма (таймаут, аппаратная ошибка)
	 *
	 */
    int32_t (*spi_receive)(SPI_HandleTypeDef* spi, uint8_t *rx_data, uint16_t len, uint32_t timeout);
    /**
	 * @brief Запись состояния на абстрактный GPIO-пин.
	 *
	 * @param[in] pin   Абстрактный номер пина для выбора соответствующего порта и пина из списков
	 * @param[in] state 0 – низкий уровень, 1 – высокий уровень
	 *
	 * @note Соответствие абстрактного номера реальному пину определяется
	 *       пользователем в реализации.
	 */
    void (*gpio_write)(uint8_t pin, uint8_t state);
    /**
	 * @brief Чтение состояния абстрактного GPIO-пина.
	 *
	 * @param[in] pin Абстрактный номер пина для выбора соответствующего порта и пина из списков
	 *
	 * @return 0 – низкий уровень, 1 – высокий уровень
	 */
    uint8_t (*gpio_read)(uint8_t pin);

    /**
	 * @brief Задержка в миллисекундах.
	 *
	 * @param[in] ms Количество миллисекунд для задержки
	 */
    void (*delay_ms)(uint32_t ms);
    
    /**
	* @brief Получение текущего времени в миллисекундах (обычно SysTick).
	*
	* @return Количество миллисекунд от запуска системы
	*/
    uint32_t (*get_tick_ms)(void);
} bn008x_hal_t;
/**
 * @brief Инициализация HAL-структуры.
 *
 * Заполняет структуру bn008x_hal_t указателями на функции.
 *
 * @param[out] hal Указатель на структуру, которую нужно заполнить
 *
 */
void bn008x_hal_init(bn008x_hal_t *hal);
#endif
